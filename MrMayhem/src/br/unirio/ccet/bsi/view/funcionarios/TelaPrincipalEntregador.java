    private boolean avisar = true;

    public TelaPrincipalEntregador() {
        initComponents();
        
        addWindowStateListener((WindowEvent arg0) -> {
            if (arg0.equals(WindowEvent.WINDOW_CLOSING)){
                avisar = false;
            }
        });
        
        Thread t = new Thread(){
            @Override
            public void run() {
                XmlEntrega xml = new XmlEntrega();
                File arquivos = new File(Utils.recuperarPath("Entregas"));
                String[] numsPedidos = arquivos.list();
                while(avisar){
                    for (String numPedido : numsPedidos){
                        Entrega dadosEntrega = xml.LerXml(numPedido);
                        if (dadosEntrega.getStatus().equals(Enums.Status.PENDENTE)){
                            JOptionPane.showMessageDialog(getParent(), "VocÃª possui entregas pendentes!");
                            avisar = true;
                        } else {
                            avisar = false;
                        }
                    }
                    try {
                        Thread.sleep(10 * 1000);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
        };
        t.start();
        
    }

